package org.tnsif.wrapperclassdemo;

/*Program to demonstrate on autoboxing
 * Autoboxing- is conversion of primitive type to object type*/ 

public class Autoboxing {

	public static void main(String[] args) {
		
		char c='A';
		
		//converting primitive data type to object type
		Character ch=c;
		System.out.println(ch);

	}

}
