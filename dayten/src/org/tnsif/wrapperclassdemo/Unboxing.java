package org.tnsif.wrapperclassdemo;

/*Program to demonstrate on autoboxing
 * Unboxing- is conversion of  object type to primitive type */ 

public class Unboxing {

	public static void main(String[] args) {
		
		Integer num=12;
		
		//converting  type  object type to primitive data
		int no=num;
		System.out.println(no);

	}

}
